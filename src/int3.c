#include "ft_math.h"

t_int3	uint3_add(t_int3 l, t_int3 r)
{
	return ((t_int3){l.x + r.x, l.y + r.y, l.z + r.z});
}

t_int3	uint3_sub(t_int3 l, t_int3 r)
{
	return ((t_int3){l.x - r.x, l.y - r.y, l.z - r.z});
}
